# PATTERN 366: Signal-to-Noise Ratio (SNR)
# =============================================================================

def test_signal_to_noise_ratio(muqattaat_sequences):
    """
    Pattern #366: Signal-to-Noise Ratio
    Test SNR for letter frequency signals.
    """
    all_letters = "".join(muqattaat_sequences.values())
    counter = Counter(all_letters)
    
    # Signal = most frequent letter
    most_common = counter.most_common(1)[0][1]
    
    # Noise = standard deviation of frequencies
    freqs = list(counter.values())
    mean_freq = sum(freqs) / len(freqs)
    noise = (sum((f - mean_freq) ** 2 for f in freqs) / len(freqs)) ** 0.5
    
    if noise > 0:
        snr = 10 * math.log10(most_common / noise)
        print(f"Signal-to-Noise Ratio: {snr:.2f} dB")
    else:
        print("SNR: infinite (no noise)")


