# PATTERN 361: Autocorrelation Coefficient
# =============================================================================

def test_autocorrelation_coefficient(muqattaat_sequences):
    """
    Pattern #361: Autocorrelation Coefficient
    Test letter sequence autocorrelation.
    """
    for surah, sequence in list(muqattaat_sequences.items())[:5]:
        # Simple autocorrelation: compare sequence with itself shifted
        seq_bytes = [ord(c) for c in sequence]
        
        if len(seq_bytes) > 1:
            # Compare first half to second half
            mid = len(seq_bytes) // 2
            first_half = seq_bytes[:mid]
            second_half = seq_bytes[mid:]
            
            # Calculate correlation
            mean1 = sum(first_half) / len(first_half)
            mean2 = sum(second_half) / len(second_half)
            
            numerator = sum((a - mean1) * (b - mean2) for a, b in zip(first_half, second_half))
            denom1 = sum((a - mean1) ** 2 for a in first_half) ** 0.5
            denom2 = sum((b - mean2) ** 2 for b in second_half) ** 0.5
            
            if denom1 > 0 and denom2 > 0:
                corr = numerator / (denom1 * denom2)
                print(f"Surah {surah}: autocorrelation = {corr:.3f}")
            else:
                print(f"Surah {surah}: insufficient data for autocorrelation")


