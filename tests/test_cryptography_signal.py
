# PATTERN 390: Avalanche Effect Weight
# =============================================================================

def test_avalanche_effect_weight():
    """
    Pattern #390: Avalanche Effect Weight
    Test that small changes cause large hash changes.
    """
    original = "الم"
    hash1 = hashlib.sha256(original.encode()).hexdigest()
    
    # Flip one bit via character change
    modified = "الـ"  # Slightly different
    hash2 = hashlib.sha256(modified.encode()).hexdigest()
    
    # Count differing bits
    diff_bits = sum(bin(int(h1, 16) ^ int(h2, 16)).count('1') for h1, h2 in zip(hash1, hash2))
    total_bits = len(hash1) * 4  # Each hex char = 4 bits
    
    avalanche = diff_bits / total_bits
    print(f"Avalanche effect: {avalanche:.1%} bits changed")


