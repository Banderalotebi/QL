        values = [ord(c) for c in sequence]
        n = len(values)
        
        # Calculate power spectrum
        power_spectrum = []
        for k in range(n):
            real = sum(values[t] * math.cos(2 * math.pi * k * t / n) for t in range(n))
            imag = sum(values[t] * math.sin(2 * math.pi * k * t / n) for t in range(n))
            power = (real**2 + imag**2) / n**2
            power_spectrum.append(power)
        
        print(f"Surah {surah}: max power = {max(power_spectrum):.2f}")


