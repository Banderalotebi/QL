# PATTERN 369: Viterbi Decoding Path
# =============================================================================

def test_viterbi_decoding_path():
    """
    Pattern #369: Viterbi Decoding Path
    Test path optimization in HMM.
    """
    # Simplified Viterbi for sequence "الم"
    states = ['A', 'L', 'M']  # States match letters
    obs = [1, 30, 40]  # Abjad values as observations
    
    # Transition probabilities (simplified uniform)
    trans = {
        ('A', 'L'): 0.5,
        ('L', 'M'): 0.5,
    }
    
    # Emission probabilities
    emit = {
        ('A', 1): 0.8,
        ('L', 30): 0.8,
        ('M', 40): 0.8,
    }
    
    # Forward pass
    viterbi = [{}]
    for i, state in enumerate(states):
        viterbi[0][state] = emit.get((state, obs[i]), 0.1)
    
    for t in range(1, len(obs)):
        viterbi.append({})
        for state in states:
            max_prob = max(
                viterbi[t-1][prev_state] * trans.get((prev_state, state), 0.1)
                for prev_state in states
            )
            viterbi[t][state] = max_prob * emit.get((state, obs[t]), 0.1)
    
    best_path = max(viterbi[-1].items(), key=lambda x: x[1])
    print(f"Viterbi best path: {best_path[0]} with prob {best_path[1]:.4f}")


