# PATTERN 100: Signature Verification
# =============================================================================

def test_signature_verification(muqattaat_sequences):
    """
    Pattern #100: Signature Verification
    Test cryptographic signature of Muqattaat sequences.
    """
    import hmac
    
    secret_key = "muqattaat_integrity_key"
    
    for surah, sequence in muqattaat_sequences.items():
        # Create HMAC signature
        signature = hmac.new(
            secret_key.encode(),
            sequence.encode(),
            hashlib.sha256
        ).hexdigest()
        
        # Verify signature
        verify = hmac.compare_digest(
            hmac.new(secret_key.encode(), sequence.encode(), hashlib.sha256).hexdigest(),
            signature
        )
        
        assert verify, f"Signature verification failed for Surah {surah}"
        print(f"Surah {surah}: signature verified ({signature[:16]}...)")
    
    print("Signature verification: All signatures valid")


