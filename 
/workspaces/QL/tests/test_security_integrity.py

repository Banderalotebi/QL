def test_recursive_loop_prevention():
    """
    Pattern #106: Recursive Loop Prevention
    Test prevention of infinite recursion in analysis.
    """
    max_depth = 100
    
    def recursive_analysis(depth=0):
        if depth >= max_depth:
            return "MAX_DEPTH_REACHED"
        if depth < 3:
            return recursive_analysis(depth + 1)
        return depth
    
    result = recursive_analysis()
    assert result == "MAX_DEPTH_REACHED" or result >= 0
    print(f"Recursive loop prevention: max_depth={max_depth} enforced")


