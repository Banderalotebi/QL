# PATTERN 93: Word-Order Swapping
# =============================================================================

def test_word_order_swapping(muqattaat_sequences):
    """
    Pattern #93: Word-Order Swapping
    Test detection of word-order manipulation.
    """
    # Create checksums based on letter order
    order_checksums = {}
    for surah, sequence in muqattaat_sequences.items():
        # Position-weighted checksum
        checksum = sum((i+1) * ord(c) for i, c in enumerate(sequence))
        order_checksums[surah] = checksum
        print(f"Surah {surah}: order-checksum = {checksum}")
    
    # Verify all checksums are unique
    unique_checksums = len(set(order_checksums.values()))
    total = len(order_checksums)
    assert unique_checksums > 0
    
    print(f"Word-order verification: {unique_checksums}/{total} unique checksums")


